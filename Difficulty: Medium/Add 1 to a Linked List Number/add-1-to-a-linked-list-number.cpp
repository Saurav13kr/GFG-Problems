class Solution {
  private:
    int f(Node *node){
        int sum=0;
        if(node->next==NULL) sum=node->data+1;
        else sum=node->data + f(node->next);
        node->data=sum%10;
        sum/=10;
        return sum;
    }
  public:
    Node* addOne(Node* head) {
        int x=f(head);
        if(x==0) return head;
        else {
            Node *newHead=new Node(x);     
            newHead->next=head;
            return newHead;
        }
    }
};


