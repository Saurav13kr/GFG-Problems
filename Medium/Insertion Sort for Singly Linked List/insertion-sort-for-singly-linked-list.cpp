class Solution
{
    public:
    Node* insertionSort(struct Node* head_ref)
    {
        Node* head = new Node(-1);
        Node* curr = head_ref;
        while(curr)
        {
            Node* temp = head;
            while(temp->next && curr->data > temp->next->data)
            {
                temp = temp->next;
            }
            Node* temp_curr = curr;
            curr = curr->next;
            temp_curr->next = temp->next;
            temp->next = temp_curr;
        }
        return head->next;
    }
    
};


